package com.marvintdawson.emailAdminApp.UserInfo;

public class UserInfo {

    private int mobileNumber;
    private String userDevice;
    private String userDeviceType;
    private String userConnectionType;
    private String userLocation;
    private String userEmailCarrier;
    private String userAlternativeEmail;
    private String userAlternativeEmailCarrier;

    public int getMobileNumber() {
        return mobileNumber;
    }

    public void setMobileNumber(int mobileNumber) {
        this.mobileNumber = mobileNumber;
    }

    public String getUserDevice() {
        return userDevice;
    }

    public void setUserDevice(String userDevice) {
        this.userDevice = userDevice;
    }

    public String getUserDeviceType() {
        return userDeviceType;
    }

    public void setUserDeviceType(String userDeviceType) {
        this.userDeviceType = userDeviceType;
    }

    public String getUserConnectionType() {
        return userConnectionType;
    }

    public void setUserConnectionType(String userConnectionType) {
        this.userConnectionType = userConnectionType;
    }

    public String getUserLocation() {
        return userLocation;
    }

    public void setUserLocation(String userLocation) {
        this.userLocation = userLocation;
    }

    public String getUserEmailCarrier() {
        return userEmailCarrier;
    }

    public void setUserEmailCarrier(String userEmailCarrier) {
        this.userEmailCarrier = userEmailCarrier;
    }

    public String getUserAlternativeEmail() {
        return userAlternativeEmail;
    }

    public void setUserAlternativeEmail(String userAlternativeEmail) {
        this.userAlternativeEmail = userAlternativeEmail;
    }

    public String getUserAlternativeEmailCarrier() {
        return userAlternativeEmailCarrier;
    }

    public void setUserAlternativeEmailCarrier(String userAlternativeEmailCarrier) {
        this.userAlternativeEmailCarrier = userAlternativeEmailCarrier;
    }
}
